/**
 * Theme mode for the app.
 */
enum class ThemeMode {
    LIGHT,
    DARK,
    SYSTEM
}

/**
 * Main theme for the demo app with support for light/dark modes.
 *
 * @param themeMode The theme mode to use (LIGHT, DARK, or SYSTEM)
 * @param content The composable content to be themed
 */
@Composable
fun NavPlaygroundTheme(
    themeMode: ThemeMode = ThemeMode.SYSTEM,
    content: @Composable () -> Unit
) {
    val useDarkTheme = when (themeMode) {
        ThemeMode.LIGHT -> false
        ThemeMode.DARK -> true
        ThemeMode.SYSTEM -> isSystemInDarkTheme()
    }

    val colorScheme = if (useDarkTheme) {
        DarkColorScheme
    } else {
        LightColorScheme
    }

    // Configure system UI (status bar, navigation bar) based on theme
    ConfigureSystemUI(useDarkTheme = useDarkTheme)

    MaterialTheme(
        colorScheme = colorScheme,
        content = content
    )
}
